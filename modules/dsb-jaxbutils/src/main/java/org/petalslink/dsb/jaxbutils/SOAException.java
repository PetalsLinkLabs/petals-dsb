package org.petalslink.dsb.jaxbutils;

public class SOAException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public SOAException() {
		super();
	}

	public SOAException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public SOAException(String arg0) {
		super(arg0);
	}

	public SOAException(Throwable arg0) {
		super(arg0);
	}

	
	
}
