package org.petalslink.dsb.notification.commons;

/**
 * @author chamerling
 *
 */
public class NotificationException extends Exception {

	public NotificationException() {
		super();
	}

	public NotificationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public NotificationException(String arg0) {
		super(arg0);
	}

	public NotificationException(Throwable arg0) {
		super(arg0);
	}

	/**
	 * 
	 */
	private static final long serialVersionUID = -5951963250120247122L;

}
